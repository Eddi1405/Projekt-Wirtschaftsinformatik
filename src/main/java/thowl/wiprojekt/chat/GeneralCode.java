package thowl.wiprojekt.chat;

/**
 * Contains general error codes.
 *
 * @version 09.08.2023
 */
public final class GeneralCode {

	/**
	 * Signifies success.
	 */
	public static final int OK = 0;

	/**
	 * Signifies an error.
	 */
	public static final int ERROR = 1;

	/**
	 * Signifies a warning.
	 */
	public static final int WARN = 2;

	/**
	 * Constructor of the class. It is private because it would make no
	 * sense to instantiate this class.
	 */
	private GeneralCode() {

	}

}
